package lr8.Task1.Example5;

import java.io.*;
import java.util.Scanner;

public class Files_byteRW_my2 {
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);
        System.out.print("¬ведите им€ файла => ");
        String fname=sc.nextLine();
        try{
            // —оздаетс€ файл
            File f1=new File(fname);
            f1.createNewFile(); // файл создан
            System.out.println("ѕолный путь файла: "+ f1.getAbsolutePath());
            System.out.print("¬ведите количество строк дл€ записи в файл => ");
            int n=sc.nextInt();
            // —оздаетс€ поток дл€ записи с учетом типа данных Ц DataOutputStream,
            // и ему в качестве параметра передаетс€ поток FileOutputStream
            DataOutputStream dOut= new DataOutputStream( new FileOutputStream(f1));
            sc.nextLine(); //очистка буфера
            for (int i = 0; i < n; i++) {
                System.out.print("¬ведите строку дл€ записи в файл => ");
                String s=sc.nextLine();
                dOut.writeUTF(s );
                //или dOut.writeUTF(s +"\n" ); Ц запись отдельных строк
            }
            dOut.flush(); // дописываем несохраненные данные на диск
            dOut.close(); // закрываем поток
            // „тение и вывод данных из созданного файла
            DataInputStream dIn=new DataInputStream(new FileInputStream(f1));
            while (true) {
                System.out.println(dIn.readUTF());
            }
        }catch (Exception e) { System.out.println(""+e);
        }
    }
}
